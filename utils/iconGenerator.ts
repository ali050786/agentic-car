import { icons } from 'lucide-react';

/**
 * Generates an SVG string for a Lucide icon.
 * @param iconName - The name of the Lucide icon (e.g., "Lightbulb", "Target")
 * @param size - Size of the icon in pixels
 * @param color - Color of the icon (CSS color value)
 * @returns SVG string or empty string if icon not found
 */
export const generateIconSVG = (
    iconName?: string,
    size: number = 80,
    color: string = '#141414'
): string => {
    if (!iconName) {
        return '';
    }

    // Get the icon from lucide-react
    const LucideIcon = icons[iconName as keyof typeof icons];

    if (!LucideIcon) {
        console.warn(`[iconGenerator] Icon "${iconName}" not found in Lucide library`);
        return '';
    }

    try {
        // Icon should be 60% of circle size for proper padding
        const iconSize = size * 0.6;

        // Return SVG that will be centered by flex container
        return `
      <svg width="${iconSize}" height="${iconSize}" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" xmlns="http://www.w3.org/2000/svg">
        ${getIconPath(iconName)}
      </svg>
    `;
    } catch (error) {
        console.error(`[iconGenerator] Error generating icon "${iconName}":`, error);
        return '';
    }
};

